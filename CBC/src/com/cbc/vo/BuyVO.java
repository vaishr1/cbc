package com.cbc.vo;

public class BuyVO {
	private String userId,nstock,punit,total,tai,chargeBack,balanceAmount;

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getNstock() {
		return nstock;
	}

	public void setNstock(String nstock) {
		this.nstock = nstock;
	}

	public String getPunit() {
		return punit;
	}

	public void setPunit(String punit) {
		this.punit = punit;
	}

	public String getTotal() {
		return total;
	}

	public void setTotal(String total) {
		this.total = total;
	}

	public String getTai() {
		return tai;
	}

	public void setTai(String tai) {
		this.tai = tai;
	}

	public String getChargeBack() {
		return chargeBack;
	}

	public void setChargeBack(String chargeBack) {
		this.chargeBack = chargeBack;
	}

	public String getBalanceAmount() {
		return balanceAmount;
	}

	public void setBalanceAmount(String balanceAmount) {
		this.balanceAmount = balanceAmount;
	}
	
	
}
